#include "libft.h"

int		ft_count_digit(int nb)
{
	int i;
	int j;
	int neg;

	i = 0;
	j = nb;
	neg = 0;
	if (nb == 0)
		return (1);
	if (j < 0)
	{
		j = j * -1;
		neg = 1;
	}
	while (j > 0)
	{
		j = j / 10;
		i++;
	}
	return (i + neg);
}
