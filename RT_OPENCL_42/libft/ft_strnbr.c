#include "libft.h"

char	*ft_strnbr(char *str, int n)
{
	int i;
	int flag;

	i = 0;
	flag = 0;
	if (n < 0)
	{
		n = n * -1;
		str[ft_count_digit(n)] = '-';
		flag = 1;
	}
	while (n != 0)
	{
		str[i] = n % 10 + '0';
		n = n / 10;
		i++;
	}
	if (flag == 1)
	{
		str[i] = '-';
		i++;
	}
	str[i] = '\0';
	return (str);
}
