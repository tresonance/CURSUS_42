#include "libft.h"

int		ft_atoi(char *str)
{
	int		i;

	i = 0;
	while ((*str >= 0 && *str <= 26) || (*str >= 28 && *str <= 32))
		str++;
	if (*str == '-')
	{
		str++;
		while (*str >= '0' && *str <= '9' && *str)
			i = (i * 10) + *str++ - '0';
		return (-i);
	}
	if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9' && *str)
		i = (i * 10) + *str++ - '0';
	return (i);
}
