int	ft_wordnb(const char *s, char c)
{
	int i;
	int nb;

	i = 0;
	nb = 0;
	if (*s == 0)
		return (0);
	while (s[i])
	{
		while (s[i] == c)
			i++;
		if (s[i] != c && s[i])
			nb++;
		while (s[i] != c && s[i])
			i++;
	}
	return (nb);
}
