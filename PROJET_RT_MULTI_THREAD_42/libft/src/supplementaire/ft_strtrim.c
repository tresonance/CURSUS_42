#include "libft.h"

char	*ft_strtrim(char const *s)
{
	char	*a;
	int		i;

	if (!s)
		return (NULL);
	a = (char*)s;
	while (*a == ' ' || *a == '\n' || *a == '\t')
		a++;
	i = ft_strlen(a) - 1;
	while ((a[i] == ' ' || a[i] == '\n' || a[i] == '\t') && i >= 0)
		i--;
	if (i < 0)
	{
		a = malloc(1);
		*a = '\0';
		return (a);
	}
	return (ft_strsub(a, 0, i + 1));
}
