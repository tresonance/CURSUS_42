#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	char			*d;
	unsigned int	i;

	d = s;
	i = 0;
	if (!s || !f)
		return ;
	while (d[i])
	{
		f(i, d + i);
		++i;
	}
}
