#include "libft.h"

int		ft_atoi(char *str)
{
	int result;
	int negatif;

	result = 0;
	while (ft_iswhitespace(*str))
		str++;
	negatif = (*str == '-');
	if (*str == '-' || *str == '+')
		str++;
	while (ft_isdigit(*str))
	{
		result = result * 10 + (*str - '0');
		str++;
	}
	return (negatif ? -1 * result : result);
}
