#include "rt.h"

void	clean_double_tab(char **tab, char **tab1)
{
	int	i;

	i = 0;
	while (tab[i] != NULL)
	{
		free(tab[i]);
		i += 1;
	}
	free(tab);
	i = 0;
	while (tab1[i] != NULL)
	{
		free(tab1[i]);
		i += 1;
	}
}
